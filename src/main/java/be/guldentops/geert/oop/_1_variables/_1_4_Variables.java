package be.guldentops.geert.oop._1_variables;

public class _1_4_Variables {

	public static void main(String[] args) {
		double a = 42.0;
		double b = 33.0;

		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / b); // Does NOT lose precision anymore!
	}
}
